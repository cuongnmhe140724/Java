    public boolean isExist(String xBookID) {
        int n, i;
        String s;
        n = tbl1.getRowCount();
        for (i = 0; i < n; i++) {
            s = (String) tbl1.getValueAt(i, 0);

            if (s != null && s.trim().equalsIgnoreCase(xBookID.trim())) {
                return (true);
            }
        }
        return (false);
    }

    public void addNewRow(Object[] a) {
        String xBookID = (String) a[0];
        if (isExist(xBookID)) {
            JOptionPane.showMessageDialog(null, "The code " + xBookID + " already exists, cannot add!");
            return;
        }
        t.addRow(a);
    }
