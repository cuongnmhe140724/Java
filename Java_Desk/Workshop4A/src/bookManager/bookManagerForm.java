    void tableToFile(String fname) throws IOException {
        File f = new File(fname);
        if (f.exists()) {
            f.delete();
        }
        RandomAccessFile raf = new RandomAccessFile(fname, "rw");
        int n, i;
        n = tbl1.getRowCount();
        if (n == 0) {
            return;
        }
        String xBookID;
        String xBookName;
        int xQuantity;
        int xPrice;

        for (i = 0; i < n; i++) {
            xBookID = (String) tbl1.getValueAt(i, 0);
            xBookName = (String) tbl1.getValueAt(i, 1);
            xQuantity = (int) tbl1.getValueAt(i, 2);
            xPrice = (int) tbl1.getValueAt(i, 3);
            raf.writeBytes(xBookID + " | " + xBookName + " | " + xQuantity + " | " + xPrice + "\r\n");
        }
        raf.close();
    }
