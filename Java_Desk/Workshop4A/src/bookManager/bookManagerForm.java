    void fileToTable(String fname) throws IOException {
        RandomAccessFile f = new RandomAccessFile(fname, "r");
        String s;
        String[] a;
        String bookID, bookName;
        int quantity, price;
        while (true) {
            s = f.readLine();
            if (s == null || s.length() < 6) {
                break;
            }
            a = s.split("[|]");
            bookID = a[0].trim();
            bookName = a[1].trim();
            quantity = Integer.parseInt(a[2].trim());
            price = Integer.parseInt(a[3].trim());
            Object[] b = {bookID, bookName, quantity, price};
            t.addRow(b);
        }
        f.close();
    }
    
    private Object[] getBookInfo() {
        String errMsg = "";
        String xBookID = txtBookID.getText().trim();
        String xBookName = txtBookName.getText().trim();
        int xQuantity = 0;
        int xPrice = 0;
        if (xBookID.isEmpty()) {
            errMsg += "Book ID is empty\r\n";
        }
        if (xBookName.isEmpty()) {
            errMsg += "Book Name is empty\r\n";
        }
        try {
            xQuantity = Integer.parseInt(txtQuantity.getText().trim());
        } catch (NumberFormatException e) {
            errMsg += "Quantity must be a positive integer\r\n";
        }

        try {
            xPrice = Integer.parseInt(txtPrice.getText().trim());
        } catch (NumberFormatException e) {
            errMsg += "Price must be a positive double\r\n";
        }
        if (!errMsg.isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(rootPane, errMsg);
            return null;
        } else {
            Object o[] = {xBookID, xBookName, xQuantity, xPrice};
            return o;
        }
    }
