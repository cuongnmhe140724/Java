    private void btnDeleteByNameActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteByNameActionPerformed
        String xName, sName;
        xName = txtNameSearch.getText().trim();
        int i, n, k;
        i = list.searchByName(xName);
        list.delete(i);

        n = tbl1.getRowCount();
        k = -1;
        for (i = 0; i < n; i++) {
            sName = (String) tbl1.getValueAt(i, 1);
            sName = sName.trim();
            if (sName.equals(xName)) {
                k = i;
            }
        }
        if (k > -0) {
            t.removeRow(k);
        }

    }//GEN-LAST:event_btnDeleteByNameActionPerformed

