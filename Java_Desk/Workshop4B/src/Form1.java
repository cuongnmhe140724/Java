    void fileToTable(String fname) throws IOException {
        RandomAccessFile f = new RandomAccessFile(fname, "r");
        String s;
        String[] a;
        String xName, xCode, xSex;
        double xSalary, xBonus, xIncome;
        while (true) {
            s = f.readLine();
            if (s == null || s.length() < 6) {
                break;
            }
            a = s.split("[|]");
            xCode = a[0].trim();
            xName = a[1].trim();
            xSex = a[2].trim();
            xSalary = Double.parseDouble(a[3].trim());
            xBonus = Double.parseDouble(a[4].trim());
            xIncome = Double.parseDouble(a[5].trim());
            Object[] b = {xCode, xName, xSex, xSalary, xBonus, xIncome};
            t.addRow(b);
        }
        f.close();
    }
