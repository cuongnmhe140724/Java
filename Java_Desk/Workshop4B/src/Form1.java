    void clearTable() {
        int n, i;
        n = tbl1.getRowCount();
        for (i = 0; i < n; i++) {
            t.removeRow(0);
        }

    }
    
    void updateTable(EmployeeList v) {        
        int n, i;
        Employee x;
        n = v.size();
        for (i = 0; i < n; i++) {
            x = v.get(i);
            t.insertRow(tbl1.getRowCount(), new Object[6]);
            tbl1.setValueAt(x.code, i, 0);
            tbl1.setValueAt(x.name, i, 1);
            tbl1.setValueAt(x.sex, i, 2);
            tbl1.setValueAt(x.salary, i, 3);
            tbl1.setValueAt(x.bonus, i, 4);
            tbl1.setValueAt(x.income, i, 5);
        }
    }
