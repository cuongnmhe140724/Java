    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        String xCode = txtCode.getText().trim();
        String xName = txtName.getText().trim();
        boolean sSex = rdtbnMale.isSelected();
        String xSex = "";
        String sSalary = txtSalary.getText().trim();

        String sBonus = txtBonus.getText().trim();

        boolean isOk = true;
        if (xCode.equals("")) {
            isOk = false;
        }
        if (xName.equals("")) {
            isOk = false;
        }
        if (sSalary.equals("")) {
            isOk = false;
        }
        if (sBonus.equals("")) {
            isOk = false;
        }

        if (!isOk) {
            JOptionPane.showMessageDialog(null, "The data cannot be empty");
            return;
        }
        if (sSex) {
            xSex = "Male";
        } else {
            xSex = "Female";
        }
        double xSalary = Double.parseDouble(sSalary);
        double xBonus = Double.parseDouble(sBonus);
        double xIncome = xSalary + xBonus;
        list.add(new Employee(xCode, xName, xSex, xSalary, xBonus, xIncome));
        Object[] a = {xCode, xName, xSex, xSalary, xBonus, xIncome};
        t.addRow(a);
    }//GEN-LAST:event_btnAddActionPerformed

